arr = [0xAD, 0xD8, 0xCB, 0xCB, 0x9D, 0x97, 0xCB, 0xC4, 0x92, 0xA1, 0xD2, 0xD7, 0xD2, 0xD6, 0xA8, 0xA5, 0xDC, 0xC7, 0xAD, 0xA3, 0xA1, 0x98, 0x4C]
ans = []

for i in range(len(arr) - 1, -1, -1):
    if i == len(arr) - 1:
        cha = chr(arr[i] - 0)
    else:
        cha = chr(arr[i] - ord(ans[0]))
    ans.insert(0, cha)

print(''.join(ans))
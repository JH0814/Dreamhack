data = [
    0x000000AE, 0x0000006D, 0x0000009B, 0x00000092,
    0x00000013, 0x0000002B, 0x000000C6, 0x000000C9,
    0x000000E5, 0x000000FA, 0x00000096, 0x0000000B,
    0x00000064, 0x00000031, 0x000000B8, 0x00000008,
    0x000000C8, 0x00000048, 0x000000D2, 0x00000030,
    0x00000060, 0x00000004, 0x000000FA, 0x0000007B,
    0x00000088, 0x000000B0, 0x0000002F, 0x0000007C,
    0x000000B3, 0x000000B3, 0x00000058, 0x00000061,
]

res = []

for i in range(len(data)):
    tmp = (data[i] - 34) & 0xFF ^0x63
    val = 0
    val |= (tmp & 1) << 1
    val |= (tmp & 2) << 2
    val |= (tmp & 4) << 4
    val |= (tmp & 8) >> 3
    val |= (tmp & 16) >> 2
    val |= (tmp & 32) << 2
    val |= (tmp & 64) >> 1
    val |= (tmp & 128) >> 3
    res.append(f"{val:02x}")
print("".join(res))
